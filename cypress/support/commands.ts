/// <reference types="cypress" />

declare namespace Cypress {
  interface Chainable {
    getInput(name: string): Chainable<JQuery<HTMLElement>>
    getSelect(name: string): Chainable<JQuery<HTMLElement>>
  }
}
Cypress.Commands.add('getInput', (name) => {
  return cy.get(`input[name="${name}"]`)
})

Cypress.Commands.add('getSelect', (name) => {
  return cy.get(`select[name="${name}"]`)
})

